// SUM OF TWO NUMBERS ------------------

#include <iostream>
using namespace std;

int main(){
    int a, b;

    cout << "Enter value of a: ";
    cin >> a;
    cout << "Enter value of b: ";
    cin >> b;

    int sum = a + b;
    cout << "Sum of " << a << " and " << b << " = " << sum;

    return 0;
}